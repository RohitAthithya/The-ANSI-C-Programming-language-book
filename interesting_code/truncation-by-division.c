//truncation: division operator

#include <stdio.h>
 
 
int main()
{

    printf("%d\n", 5/9);
    printf("%d\n", 5.0/9);
    printf("%d\n", 5/9.0);
    printf("%d\n", 5.0/9.0);
    printf("%lf\n", 5/9);
    printf("%lf\n", 5.0/9);
    printf("%lf\n", 5/9.0);
    printf("%lf\n", 5.0/9.0);
    
 
return 0;
}